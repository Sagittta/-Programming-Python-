#가변 인수
print("가변인수----------------------------")
print("♡")
print("♡", "♪")
print("♡", "♪", "♧")
print("♡", "♪", "♧", "♤")
print("♡", "♪", "♧", "♤", "☆")

def p(*args):
    st = ""
    for a in args:
        st += a
    print(st)
p("♡")
p("♡", "♪", "♧", "♤")
p("♡", "♪", "♧", "♤", "☆")

#안녕()함수
def 안녕(*args):
    for a in args:
        print("안녕,",a)

안녕("가연아", "수빈아", "정윤아")      #순서가 중요.